package agostino.romanelli.harbour;

public class PortoNavale {
	
	String nome;
	int numeroImbarcazioni;
	int mQuadrati;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getNumeroImbarcazioni() {
		return numeroImbarcazioni;
	}
	public void setNumeroImbarcazioni(int numeroImbarcazioni) {
		this.numeroImbarcazioni = numeroImbarcazioni;
	}
	public int getmQuadrati() {
		return mQuadrati;
	}
	public void setmQuadrati(int cnQuadrati) {
		this.mQuadrati = cnQuadrati;
	}

}
